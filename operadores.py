a=20
b=8

print ("***Valores default***")
print("a:",a)
print("b:",b)

print ("***Operadores aritméticos***")

suma            =   a+b
resta           =   a-b
multi           =   a*b
division        =   a/b
division_entera =   a//b
modulo          =   a%b
potencia        =   a**b

print ("suma: ",suma)
print ("resta: ",resta)
print ("multiplicación: ",multi)
print ("división: ",division)
print ("división entera: ",division_entera)
print ("módulo: ",modulo)
print ("potencia: ",potencia)

print ("***Operadores de comparación***")
igual               =a==b
diferente           =a!=b
mayor_que           =a>b
menor_que           =a<b
mayor_igual_que     =a>=b
menor_igual_que     =a<=b

print ("igual: ",igual)
print ("diferente: ",diferente)
print ("mayor_que: ",mayor_que)
print ("menor_que: ",menor_que)
print ("mayor_igual_que: ",mayor_igual_que)
print ("menor_igual_que: ",menor_igual_que)

print ("***Operadores lógicos***")
result_and  =(a>5) and (b<5)
result_or   =(a>5) or (b<5)
result_not  =not (a>5)

print ("result_and: ",result_and)
print ("result_or: ",result_or)
print ("result_not: ",result_not)